public class TestSwitch {

    public static void main(String[] args) {
        int option = 1;
        switch (option){
            case 1:
                System.out.println("选择1");
                break;
            case 2:
                System.out.println("选择2");
                break;
            case 3:
                System.out.println("选择3");
                break;
            default:
                System.out.println("没有选择");
                break;
        }
    }

}
